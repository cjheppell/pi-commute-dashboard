def get(icon):
    if icon == 'warning':
        return warning()
    if icon == 'bus':
        return bus()
    if icon == 'hourglass':
        return hourglass()
    if icon == 'tick':
        return tick()
    if icon == 'cross':
        return cross()
    if icon == 'solid':
        return solid()
    if icon == 'cloud':
        return cloud()
    if icon == 'raincloud':
        return raincloud()
    if icon == 'drizzle':
        return drizzle()
    if icon == 'snow':
        return snow()
    if icon == 'thunderstorm':
        return thunderstorm()
    if icon == 'clear':
        return clear()
    if icon == 'question':
        return question()
    raise Exception('{0} is an unrecognised icon'.format(icon))

