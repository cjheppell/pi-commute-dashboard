def print_icon(unicorn, matrix, xOrigin, yOrigin, r1, g1, b1, r2, g2, b2, r3, g3, b3):
    for row in range(len(matrix)):
        for col in range(len(matrix[row])):
            if matrix[row][col] == 1:
                unicorn.set_pixel(xOrigin + col, yOrigin - row, r1, g1, b1)
            if matrix[row][col] == 2:
                unicorn.set_pixel(xOrigin + col, yOrigin - row, r2, g2, b2)
            if matrix[row][col] == 3:
                unicorn.set_pixel(xOrigin + col, yOrigin - row, r3, g3, b3)